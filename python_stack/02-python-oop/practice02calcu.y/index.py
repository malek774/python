num1 = float(input("enter number"))
operator = input()
num2 = float(input("enter number"))
if operator == "+":
    print (num1+num2)
elif operator == "-":
      print (num1-num2)
elif operator == "*":
      print (num1*num2)
elif operator == "/":
      print (num1/num2) 
elif operator == "^":
      print (num1**num2)  
else :
     print ( "error")        
